package ThoughtWorks04.util;

public class Utils {

    public static void printMat(int[][] mat){
        int row = mat.length;
        int col = mat[0].length;
        for (int i = 0; i < row; i++) {
            for (int j = 0; j < col; j++) {
                System.out.print(mat[i][j] + " ");
            }
            System.out.println();
        }

    }
}
